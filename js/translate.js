let LANGUAGE_DATA = DEFAULT_LANGUAGE_DATA;

async function loadLanguageData() {
  try {
    const scriptUrl = document.currentScript ? document.currentScript.src : window.location.href;
    const baseUrl = new URL('.', scriptUrl).href;
    const response = await fetch(new URL('lang.json', baseUrl).href);
    if (response.ok) {
      const json = await response.json();
      LANGUAGE_DATA = json;
    }
  } catch (error) {
    console.warn('Cannot load lang.json, using fallback translations.', error);
  }
}

function updateTexts(lang) {
  const translations = LANGUAGE_DATA[lang] || LANGUAGE_DATA.en;
  document.documentElement.lang = lang;
  document.body.dir = lang === 'fa' ? 'rtl' : 'ltr';
  document.documentElement.setAttribute('dir', document.body.dir);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (key && translations[key] !== undefined) {
      el.textContent = translations[key];
    }
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (key && translations[key] !== undefined) {
      el.placeholder = translations[key];
    }
  });

  document.querySelectorAll('[data-i18n-title]').forEach(el => {
    const key = el.dataset.i18nTitle;
    if (key && translations[key] !== undefined) {
      el.title = translations[key];
    }
  });

  document.querySelectorAll('[data-i18n-value]').forEach(el => {
    const key = el.dataset.i18nValue;
    if (key && translations[key] !== undefined) {
      el.value = translations[key];
    }
  });
}

window.loadLanguageData = loadLanguageData;
window.updateTexts = updateTexts;
